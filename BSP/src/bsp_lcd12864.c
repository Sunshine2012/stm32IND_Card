    if(isTurn)
    {
        lcdWriteByte( 0x30, 0 );
        lcdSetXy( 0, y );
        lcdWriteByte( 0x0d, 0 );
    }
    else
    {
        lcdWriteByte( 0x30, 0 );
        lcdSetXy( 0, y );
        lcdWriteByte( 0x0c, 0 );
    }
    
